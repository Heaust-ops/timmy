const router = express.Router();

const randomElem = (items) => {
    return items[Math.floor(Math.random() * items.length)];
}

// Get Member Info
router.get('/get/:memberCode', (req, res) => {
    Member
    .findOne({member: req.params.memberCode}).exec()
    .then(data => res.json(data))
    .catch(err => res.json({message: err}));
})

router.patch('/time/:memberCode', (req, res) => {
    Member
    .findOneAndUpdate({member: req.params.memberCode}, {timeArray: req.body.timeArray}).exec()
    .then(data => res.json(data))
    .catch(err => res.json({message: err}));

})

router.post('/:partyCode', (req, res) => {

    Organiser.exists({party:req.params.partyCode}, function (err, doc) { 
        if (err){ 
            console.log(err) 
        }else{ 
            if (!doc) {
                res.json({message: "party code doesn't exist"});
            } else {
                    const member = new Member({
                                party: req.params.partyCode,
                                member: "m"+Date.now().toString(16),
                                name: randomElem(adj)+randomElem(animals)
                            });

                        member
                        .save()
                        .then(data => res.json(data))
                        .catch(err => res.json({message: err}));
            }
        } 
    }); 


});

